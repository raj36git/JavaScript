//sum of two number
function sum(a,b){
    return a+b;
}
//arrow function
let sumArrow = (a,b)=>{
    return a+b;
}
console.log(sumArrow(3,4));

//multiplication of two number
function mul(a,b){
    return a*b;
}
//arrow function
let mulArrow = (a,b) => {
    return a*b;
}
console.log(mulArrow(2,3));
console.log(mulArrow);
